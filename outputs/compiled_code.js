let context = document.getElementById('canvas').getContext('2d');
let animator = new Animator();
let g = new Galapagos(context, 0, 10, 50, 50);
let t = new Turtle(context, g, 10, 10, 0);
let a = 100;
animator.addToAnimationStack(t.moveStraight.bind(t), a);
animator.addToAnimationStack(t.moveBack.bind(t), 10);
animator.addToAnimationStack(t.turnLeft.bind(t), 10);
animator.addToAnimationStack(t.turnRight.bind(t), 10);
animator.addToAnimationStack(t.takeOff.bind(t), null);
animator.addToAnimationStack(t.landing.bind(t), null);
animator.animate(null);